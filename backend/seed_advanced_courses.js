async function main() {
  console.log("🧹 Очистка старых прохождений, шагов и курсов...");
  await prisma.screenshotViolation.deleteMany({});
  await prisma.userLessonProgress.deleteMany({});
  await prisma.userCourseBlock.deleteMany({});
  await prisma.courseCompletion.deleteMany({});
  await prisma.courseStep.deleteMany({});
  await prisma.course.deleteMany({});

  console.log("🌱 Создание новых курсов с шагами, видео и экзаменами...");
  for (const c of coursesData) {
    const createdCourse = await prisma.course.create({
      data: {
        title: c.title,
        description: c.description,
        miniDescription: c.miniDescription || '',
        authorName: c.authorName || '',
        iconType: c.iconType,
        color: c.color
      }
    });

    console.log(`Created course: ${createdCourse.title} (ID: ${createdCourse.id})`);

    for (const step of c.steps) {
      await prisma.courseStep.create({
        data: {
          courseId: createdCourse.id,
          order: step.order,
          type: step.type,
          title: step.title,
          content: step.content,
          questions: step.questions,
          videoUrl: step.videoUrl || null,
          videoDurationSeconds: step.videoDurationSeconds || null,
          isFinalExam: step.isFinalExam || false,
          imageUrl: step.imageUrl || null
        }
      });
    }
  }
  console.log("✨ База данных курсов успешно заполнена!");
}

main()
  .catch(e => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
